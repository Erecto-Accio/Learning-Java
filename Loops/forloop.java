public class forloop {
    public static void main(String[] args) {
        /*
         * Basic Syntax of for loop
         * 
         * for(initialization; condition; updation){
         * 
         * // do something
         * 
         * }
         */

        for (int i = 0; i < 10; i++) {
            System.out.println("Hello,World"); // prints Hello,World 10 times
        }
    }
}
