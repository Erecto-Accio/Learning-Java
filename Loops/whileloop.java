public class whileloop {
    public static void main(String[] args) {
        /*
         * 
         * While Loop
         * 
         * syntax : while(condition){
         * // statement
         * }
         * 
         */

        int counter = 0;
        while (counter < 10) {
            System.out.println(counter);
            counter++;
        }
    }
}