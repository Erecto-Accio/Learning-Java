public class basic_function {

    // defined the function
    public static void printHelloWorld() {
        System.out.println("Hello,World");
        System.out.println("Hello,World");
        System.out.println("Hello,World");
        System.out.println("Hello,World");
    }

    public static void main(String[] args) {
        // calling the function
        printHelloWorld();
    }
}